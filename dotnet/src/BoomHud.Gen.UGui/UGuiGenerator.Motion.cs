    private static void EmitMotion(
        HudDocument document,
        PlanDocument plan,
        GenerationOptions options,
        List<Diagnostic> diagnostics,
        List<GeneratedFile> files)
    {
        var motion = options.Motion;
        if (motion == null)
        {
            return;
        }

        var resolver = new RuleResolver(options.RuleSet, "ugui");
        var effectiveMotion = MotionPolicyService.Apply(motion, resolver, document.Name);

        AddPortableMotionDiagnostics(effectiveMotion, diagnostics);

        try
        {
            var targetInfos = CollectMotionTargetInfos(plan);
            files.Add(new GeneratedFile
            {
                Path = $"{document.Name}Motion.gen.cs",
                Content = GenerateMotionClass(document, effectiveMotion, options, targetInfos, diagnostics, resolver),
                Type = GeneratedFileType.SourceCode
            });

            files.Add(new GeneratedFile
            {
                Path = $"{document.Name}MotionHost.gen.cs",
                Content = GenerateMotionHostClass(document, options),
                Type = GeneratedFileType.SourceCode
            });
        }
        catch (Exception ex)
        {
            diagnostics.Add(Diagnostic.Error($"uGUI motion export failed: {ex.Message}", code: "BHUG2000"));
        }
    }

    private static string GenerateMotionClass(
        HudDocument document,
        MotionDocument motion,
        GenerationOptions options,
        Dictionary<string, UGuiMotionTargetInfo> targetInfos,
        List<Diagnostic> diagnostics,
        RuleResolver resolver)
    {
        var builder = new StringBuilder();

        if (options.IncludeComments)
        {
            builder.AppendLine("// <auto-generated>");
            builder.AppendLine($"// Generated by BoomHud.Gen.UGui from motion document {motion.Name}");
            builder.AppendLine("// </auto-generated>");
            builder.AppendLine();
        }

        if (options.UseNullableAnnotations)
        {
            builder.AppendLine("#nullable enable");
            builder.AppendLine();
        }

        builder.AppendLine("using System;");
        builder.AppendLine("using BoomHud.Unity.Runtime;");
        builder.AppendLine("using UnityEngine;");
        builder.AppendLine("using UnityEngine.UI;");
        builder.AppendLine();
        builder.AppendLine($"namespace {options.Namespace}");
        builder.AppendLine("{");
        builder.AppendLine($"public static class {document.Name}Motion");
        builder.AppendLine("{");
        builder.AppendLine($"    public const int FramesPerSecond = {motion.FramesPerSecond.ToString(CultureInfo.InvariantCulture)};");
        builder.AppendLine($"    public const string DefaultClipId = {ToStringLiteral(motion.Clips.Count > 0 ? motion.Clips[0].Id : string.Empty)};");
        builder.AppendLine("    public static readonly string[] ClipIds =");
        builder.AppendLine("    {");
        foreach (var clip in motion.Clips)
        {
            builder.AppendLine($"        {ToStringLiteral(clip.Id)},");
        }

        builder.AppendLine("    };");
        builder.AppendLine();
        AppendMotionSequenceMetadata(builder, motion);
        builder.AppendLine($"    public static bool TryApplyAtFrame({document.Name}View view, string clipId, int frame)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (view == null)");
        builder.AppendLine("        {");
        builder.AppendLine("            throw new ArgumentNullException(nameof(view));");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return clipId switch");
        builder.AppendLine("        {");
        foreach (var clip in motion.Clips)
        {
            builder.AppendLine($"            {ToStringLiteral(clip.Id)} => Apply{Pascalize(clip.Id)}(view, frame),");
        }

        builder.AppendLine("            _ => false");
        builder.AppendLine("        };");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine($"    public static bool TryApplyAtTime({document.Name}View view, string clipId, float timeSeconds)");
        builder.AppendLine("    {");
        builder.AppendLine("        return TryApplyAtFrame(view, clipId, Mathf.Max(0, Mathf.RoundToInt(timeSeconds * FramesPerSecond)));");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    public static int GetClipDurationFrames(string clipId)");
        builder.AppendLine("    {");
        builder.AppendLine("        return clipId switch");
        builder.AppendLine("        {");
        foreach (var clip in motion.Clips)
        {
            builder.AppendLine($"            {ToStringLiteral(clip.Id)} => {clip.DurationFrames.ToString(CultureInfo.InvariantCulture)},");
        }

        builder.AppendLine("            _ => 0");
        builder.AppendLine("        };");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    public static float GetClipDurationSeconds(string clipId)");
        builder.AppendLine("    {");
        builder.AppendLine("        return GetClipDurationFrames(clipId) / (float)FramesPerSecond;");
        builder.AppendLine("    }");

        var clipInfos = motion.Clips
            .Select(clip => AnalyzeMotionClip(document, clip, targetInfos, diagnostics, resolver))
            .ToList();

        foreach (var clipInfo in clipInfos)
        {
            foreach (var field in clipInfo.ChannelFields)
            {
                builder.AppendLine();
                builder.AppendLine($"    private static readonly {field.FieldType}[] {field.FieldName} =");
                builder.AppendLine("    {");
                foreach (var item in field.Items)
                {
                    builder.AppendLine($"        {item},");
                }

                builder.AppendLine("    };");
            }

            builder.AppendLine();
            builder.AppendLine($"    private static bool Apply{Pascalize(clipInfo.Clip.Id)}({document.Name}View view, int frame)");
            builder.AppendLine("    {");
            builder.AppendLine($"        var localFrame = Mathf.Clamp(frame - {clipInfo.Clip.StartFrame.ToString(CultureInfo.InvariantCulture)}, 0, {clipInfo.Clip.DurationFrames.ToString(CultureInfo.InvariantCulture)});");
            builder.AppendLine();
            foreach (var action in clipInfo.Actions)
            {
                builder.AppendLine($"        {action}");
            }

            builder.AppendLine("        return true;");
            builder.AppendLine("    }");
        }

        AppendMotionHelpers(builder);

        builder.AppendLine("}");
        builder.AppendLine("}");
        return builder.ToString();
    }

    private static string GenerateMotionHostClass(HudDocument document, GenerationOptions options)
    {
        var builder = new StringBuilder();

        if (options.IncludeComments)
        {
            builder.AppendLine("// <auto-generated>");
            builder.AppendLine($"// Generated by BoomHud.Gen.UGui from {document.Name} motion host");
            builder.AppendLine("// </auto-generated>");
            builder.AppendLine();
        }

        if (options.UseNullableAnnotations)
        {
            builder.AppendLine("#nullable enable");
            builder.AppendLine();
        }

        builder.AppendLine("using System;");
        builder.AppendLine("using BoomHud.Unity.UGUI;");
        builder.AppendLine("using UnityEngine;");
        builder.AppendLine();
        builder.AppendLine($"namespace {options.Namespace}");
        builder.AppendLine("{");
        builder.AppendLine($"public class {document.Name}MotionHost : BoomHudUguiMotionHost");
        builder.AppendLine("{");
        builder.AppendLine($"    private {document.Name}View? _view;");
        builder.AppendLine();
        builder.AppendLine($"    public {document.Name}View View => _view ?? throw new InvalidOperationException(\"The generated view has not been bound yet.\");");
        builder.AppendLine();
        builder.AppendLine("    protected override void BindMotionView(RectTransform root)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (root == null)");
        builder.AppendLine("        {");
        builder.AppendLine("            throw new ArgumentNullException(nameof(root));");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        _view = BindExistingOrCreate(root);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    protected override bool TryApplyMotionAtTime(string clipId, float timeSeconds)");
        builder.AppendLine("    {");
        builder.AppendLine($"        return _view != null && {document.Name}Motion.TryApplyAtTime(_view, clipId, timeSeconds);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    protected override float GetClipDurationSeconds(string clipId)");
        builder.AppendLine("    {");
        builder.AppendLine($"        return {document.Name}Motion.GetClipDurationSeconds(clipId);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine($"    private static {document.Name}View BindExistingOrCreate(RectTransform root)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (TryBindExisting(root, out var boundView))");
        builder.AppendLine("        {");
        builder.AppendLine("            return boundView;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (root.childCount == 1 && root.GetChild(0) is RectTransform childRoot && TryBindExisting(childRoot, out boundView))");
        builder.AppendLine("        {");
        builder.AppendLine("            return boundView;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine($"        return new {document.Name}View(root);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine($"    private static bool TryBindExisting(RectTransform root, out {document.Name}View view)");
        builder.AppendLine("    {");
        builder.AppendLine("        try");
        builder.AppendLine("        {");
        builder.AppendLine($"            view = {document.Name}View.Bind(root);");
        builder.AppendLine("            return true;");
        builder.AppendLine("        }");
        builder.AppendLine("        catch (InvalidOperationException)");
        builder.AppendLine("        {");
        builder.AppendLine("            view = null!;");
        builder.AppendLine("            return false;");
        builder.AppendLine("        }");
        builder.AppendLine("    }");
        builder.AppendLine("}");
        builder.AppendLine("}");
        return builder.ToString();
    }

    private static void AppendMotionSequenceMetadata(StringBuilder builder, MotionDocument motion)
    {
        var defaultSequenceId = ResolveDefaultSequenceId(motion) ?? string.Empty;
        builder.AppendLine($"    public const string DefaultSequenceId = {ToStringLiteral(defaultSequenceId)};");
        builder.AppendLine("    public static readonly string[] SequenceIds =");
        builder.AppendLine("    {");
        foreach (var sequence in motion.Sequences)
        {
            builder.AppendLine($"        {ToStringLiteral(sequence.Id)},");
        }

        builder.AppendLine("    };");
        builder.AppendLine();
        builder.AppendLine("    public enum TimelineSequenceFillMode");
        builder.AppendLine("    {");
        builder.AppendLine("        None,");
        builder.AppendLine("        HoldStart,");
        builder.AppendLine("        HoldEnd,");
        builder.AppendLine("        HoldBoth");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    public sealed class TimelineSequenceClip");
        builder.AppendLine("    {");
        builder.AppendLine("        public string ClipId { get; set; } = string.Empty;");
        builder.AppendLine("        public int StartFrame { get; set; }");
        builder.AppendLine("        public int DurationFrames { get; set; }");
        builder.AppendLine("        public TimelineSequenceFillMode FillMode { get; set; } = TimelineSequenceFillMode.None;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    public static TimelineSequenceClip[] GetSequenceItems(string sequenceId)");
        builder.AppendLine("    {");
        builder.AppendLine("        return sequenceId switch");
        builder.AppendLine("        {");
        foreach (var sequence in motion.Sequences)
        {
            builder.AppendLine($"            {ToStringLiteral(sequence.Id)} => new[]");
            builder.AppendLine("            {");
            foreach (var item in sequence.Items)
            {
                builder.AppendLine("                new TimelineSequenceClip");
                builder.AppendLine("                {");
                builder.AppendLine($"                    ClipId = {ToStringLiteral(item.ClipId)},");
                builder.AppendLine($"                    StartFrame = {(item.StartFrame ?? 0).ToString(CultureInfo.InvariantCulture)},");
                builder.AppendLine($"                    DurationFrames = {(item.DurationFrames ?? 0).ToString(CultureInfo.InvariantCulture)},");
                builder.AppendLine($"                    FillMode = TimelineSequenceFillMode.{item.FillMode}");
                builder.AppendLine("                },");
            }

            builder.AppendLine("            },");
        }

        builder.AppendLine("            _ => Array.Empty<TimelineSequenceClip>()");
        builder.AppendLine("        };");
        builder.AppendLine("    }");
        builder.AppendLine();
    }

    private static UGuiMotionClipInfo AnalyzeMotionClip(
        HudDocument document,
        MotionClip clip,
        Dictionary<string, UGuiMotionTargetInfo> targetInfos,
        List<Diagnostic> diagnostics,
        RuleResolver resolver)
    {
        var fields = new List<UGuiMotionFieldInfo>();
        var actions = new List<string>();

        foreach (var track in clip.Tracks)
        {
            if (!TryResolveMotionTarget(track, document, targetInfos, resolver, out var targetInfo))
            {
                var policy = MotionPolicyService.ResolveTargetResolutionPolicy(resolver, document.Name, track, clip.Id);
                var message = $"Motion target '{track.TargetId}' was not found in HUD document '{document.Name}'.";
                diagnostics.Add(MotionPolicyService.IsErrorPolicy(policy)
                    ? Diagnostic.Error($"{message} Target resolution policy is set to error.", code: "BHUG2001")
                    : Diagnostic.Warning($"{message} It will be skipped.", code: "BHUG2001"));
                continue;
            }

            var runtimeChannels = new Dictionary<MotionProperty, UGuiMotionChannelInfo>();
            foreach (var channel in track.Channels)
            {
                if (!TryMapMotionProperty(channel.Property, targetInfo.FieldType, diagnostics, track.TargetId, resolver, document.Name, track, clip.Id))
                {
                    continue;
                }

                if (!channel.Keyframes.All(keyframe => MotionDocument.IsPortableValueKind(channel.Property, keyframe.Value.Kind)))
                {
                    continue;
                }

                var fieldName = $"s_{SanitizeIdentifier(Pascalize(clip.Id), "Clip")}{SanitizeIdentifier(Pascalize(track.Id), "Track")}{SanitizeIdentifier(channel.Property.ToString(), "Property")}";
                var fieldType = channel.Property switch
                {
                    MotionProperty.Visibility => "BooleanKeyframe",
                    MotionProperty.Text or MotionProperty.Color => "StringKeyframe",
                    _ => "NumberKeyframe"
                };

                fields.Add(new UGuiMotionFieldInfo
                {
                    FieldName = fieldName,
                    FieldType = fieldType,
                    Items = channel.Keyframes.Select(keyframe => FormatMotionKeyframe(fieldType, keyframe)).ToList()
                });

                runtimeChannels[channel.Property] = new UGuiMotionChannelInfo
                {
                    Property = channel.Property,
                    FieldName = fieldName
                };
            }

            AppendMotionActions(actions, targetInfo.Accessor, runtimeChannels);
        }

        return new UGuiMotionClipInfo
        {
            Clip = clip,
            ChannelFields = fields,
            Actions = actions
        };
    }

    private static void AppendMotionActions(
        List<string> actions,
        string accessor,
        IReadOnlyDictionary<MotionProperty, UGuiMotionChannelInfo> channels)
    {
        if (channels.TryGetValue(MotionProperty.Opacity, out var opacity))
        {
            actions.Add($"ApplyOpacity({accessor}, EvaluateNumber(localFrame, {opacity.FieldName}, 1f));");
        }

        if (channels.TryGetValue(MotionProperty.Width, out var width))
        {
            actions.Add($"ApplyWidth({accessor}, EvaluateNumber(localFrame, {width.FieldName}, 0f));");
        }

        if (channels.TryGetValue(MotionProperty.Height, out var height))
        {
            actions.Add($"ApplyHeight({accessor}, EvaluateNumber(localFrame, {height.FieldName}, 0f));");
        }

        if (channels.TryGetValue(MotionProperty.Visibility, out var visibility))
        {
            actions.Add($"ApplyVisibility({accessor}, EvaluateBoolean(localFrame, {visibility.FieldName}, true));");
        }

        if (channels.TryGetValue(MotionProperty.Text, out var text))
        {
            actions.Add($"ApplyText({accessor}, EvaluateString(localFrame, {text.FieldName}, string.Empty));");
        }

        if (channels.TryGetValue(MotionProperty.Color, out var color))
        {
            actions.Add($"ApplyColor({accessor}, EvaluateString(localFrame, {color.FieldName}, string.Empty));");
        }

        var hasPositionX = channels.TryGetValue(MotionProperty.PositionX, out var positionX);
        var hasPositionY = channels.TryGetValue(MotionProperty.PositionY, out var positionY);
        if (hasPositionX || hasPositionY)
        {
            var xExpression = hasPositionX ? $"EvaluateNumber(localFrame, {positionX!.FieldName}, 0f)" : "0f";
            var yExpression = hasPositionY ? $"EvaluateNumber(localFrame, {positionY!.FieldName}, 0f)" : "0f";
            actions.Add($"ApplyTranslate({accessor}, {xExpression}, {yExpression});");
        }

        var hasScaleX = channels.TryGetValue(MotionProperty.ScaleX, out var scaleX);
        var hasScaleY = channels.TryGetValue(MotionProperty.ScaleY, out var scaleY);
        if (hasScaleX || hasScaleY)
        {
            var xExpression = hasScaleX ? $"EvaluateNumber(localFrame, {scaleX!.FieldName}, 1f)" : "1f";
            var yExpression = hasScaleY ? $"EvaluateNumber(localFrame, {scaleY!.FieldName}, 1f)" : "1f";
            actions.Add($"ApplyScale({accessor}, {xExpression}, {yExpression});");
        }

        if (channels.TryGetValue(MotionProperty.Rotation, out var rotation))
        {
            actions.Add($"ApplyRotation({accessor}, EvaluateNumber(localFrame, {rotation.FieldName}, 0f));");
        }
    }

    private static Dictionary<string, UGuiMotionTargetInfo> CollectMotionTargetInfos(PlanDocument plan)
    {
        var targets = new Dictionary<string, UGuiMotionTargetInfo>(StringComparer.Ordinal);

        void Traverse(PlannedNode node, string accessor)
        {
            if (!string.IsNullOrWhiteSpace(node.Source.Id))
            {
                targets[node.Source.Id!] = new UGuiMotionTargetInfo
                {
                    Accessor = accessor,
                    FieldType = node.FieldType
                };
            }

            foreach (var child in node.Children)
            {
                Traverse(child, $"view.{child.FieldName}");
            }
        }

        Traverse(plan.Root, "view.Root");

        if (string.IsNullOrWhiteSpace(plan.Root.Source.Id))
        {
            targets["__root__"] = new UGuiMotionTargetInfo
            {
                Accessor = "view.Root",
                FieldType = "RectTransform"
            };
        }

        return targets;
    }

    private static bool TryResolveMotionTarget(
        MotionTrack track,
        HudDocument document,
        IReadOnlyDictionary<string, UGuiMotionTargetInfo> targetInfos,
        RuleResolver resolver,
        out UGuiMotionTargetInfo targetInfo)
    {
        if (targetInfos.TryGetValue(track.TargetId, out targetInfo!))
        {
            return true;
        }

        if (track.TargetKind == MotionTargetKind.Root)
        {
            return targetInfos.TryGetValue(document.Root.Id ?? "__root__", out targetInfo!);
        }

        return false;
    }

    private static bool TryMapMotionProperty(
        MotionProperty property,
        string fieldType,
        List<Diagnostic> diagnostics,
        string targetId,
        RuleResolver resolver,
        string documentName,
        MotionTrack track,
        string clipId)
    {
        switch (property)
        {
            case MotionProperty.Opacity:
            case MotionProperty.PositionX:
            case MotionProperty.PositionY:
            case MotionProperty.ScaleX:
            case MotionProperty.ScaleY:
            case MotionProperty.Rotation:
            case MotionProperty.Width:
            case MotionProperty.Height:
            case MotionProperty.Visibility:
            case MotionProperty.Color:
                return true;
            case MotionProperty.Text:
                if (SupportsMotionTextMutation(fieldType))
                {
                    return true;
                }

                ReportUnsupportedMotionProperty(diagnostics, resolver, documentName, track, clipId, property,
                    $"uGUI motion export does not support text mutation on target '{targetId}' ({fieldType}).");
                return false;
            default:
                ReportUnsupportedMotionProperty(diagnostics, resolver, documentName, track, clipId, property,
                    $"uGUI motion export does not support property '{property}' on target '{targetId}' yet.");
                return false;
        }
    }

    private static void ReportUnsupportedMotionProperty(
        List<Diagnostic> diagnostics,
        RuleResolver resolver,
        string documentName,
        MotionTrack track,
        string clipId,
        MotionProperty property,
        string message)
    {
        var policy = MotionPolicyService.ResolveRuntimePropertySupportFallback(resolver, documentName, track, property, clipId);
        diagnostics.Add(MotionPolicyService.IsErrorPolicy(policy)
            ? Diagnostic.Error(message, code: "BHUG2002")
            : Diagnostic.Warning(message, code: "BHUG2002"));
    }

    private static bool SupportsMotionTextMutation(string fieldType)
        => string.Equals(fieldType, "Text", StringComparison.Ordinal)
            || string.Equals(fieldType, "Button", StringComparison.Ordinal)
            || string.Equals(fieldType, "Toggle", StringComparison.Ordinal)
            || string.Equals(fieldType, "InputField", StringComparison.Ordinal);

    private static void AddPortableMotionDiagnostics(MotionDocument motion, List<Diagnostic> diagnostics)
    {
        var existing = new HashSet<(string Code, string Message)>(
            motion.LoadDiagnostics.Select(diagnostic => (diagnostic.Code, diagnostic.Message)));

        foreach (var diagnostic in motion.ValidatePortableContract())
        {
            if (!existing.Add((diagnostic.Code, diagnostic.Message)))
            {
                continue;
            }

            diagnostics.Add(Diagnostic.Warning(diagnostic.Message, diagnostic.SourceFile, diagnostic.Code));
        }
    }

    private static string FormatMotionKeyframe(string fieldType, MotionKeyframe keyframe)
        => fieldType switch
        {
            "BooleanKeyframe" => $"new BooleanKeyframe({keyframe.Frame.ToString(CultureInfo.InvariantCulture)}, {Bool(keyframe.Value.Boolean ?? false)})",
            "StringKeyframe" => $"new StringKeyframe({keyframe.Frame.ToString(CultureInfo.InvariantCulture)}, {ToStringLiteral(keyframe.Value.Text ?? string.Empty)})",
            _ => $"new NumberKeyframe({keyframe.Frame.ToString(CultureInfo.InvariantCulture)}, {ToFloatLiteral(keyframe.Value.Number ?? 0d)}, EaseMode.{keyframe.Easing})"
        };

    private static string? ResolveDefaultSequenceId(MotionDocument motion)
    {
        if (!string.IsNullOrWhiteSpace(motion.DefaultSequenceId)
            && motion.Sequences.Any(sequence => string.Equals(sequence.Id, motion.DefaultSequenceId, StringComparison.Ordinal)))
        {
            return motion.DefaultSequenceId;
        }

        return motion.Sequences.Count > 0 ? motion.Sequences[0].Id : null;
    }

    private static void AppendMotionHelpers(StringBuilder builder)
    {
        builder.AppendLine();
        builder.AppendLine("    private static float EvaluateNumber(int frame, NumberKeyframe[] keyframes, float defaultValue)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (keyframes.Length == 0)");
        builder.AppendLine("        {");
        builder.AppendLine("            return defaultValue;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (frame <= keyframes[0].Frame)");
        builder.AppendLine("        {");
        builder.AppendLine("            return keyframes[0].Value;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        for (var index = 0; index < keyframes.Length - 1; index++)");
        builder.AppendLine("        {");
        builder.AppendLine("            var current = keyframes[index];");
        builder.AppendLine("            var next = keyframes[index + 1];");
        builder.AppendLine("            if (frame > next.Frame)");
        builder.AppendLine("            {");
        builder.AppendLine("                continue;");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            if (current.Easing == EaseMode.Step || Mathf.Approximately(current.Value, next.Value))");
        builder.AppendLine("            {");
        builder.AppendLine("                return current.Value;");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            var t = Mathf.InverseLerp(current.Frame, next.Frame, frame);");
        builder.AppendLine("            return Mathf.LerpUnclamped(current.Value, next.Value, Ease(t, current.Easing));");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return keyframes[keyframes.Length - 1].Value;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static bool EvaluateBoolean(int frame, BooleanKeyframe[] keyframes, bool defaultValue)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (keyframes.Length == 0)");
        builder.AppendLine("        {");
        builder.AppendLine("            return defaultValue;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        var result = keyframes[0].Value;");
        builder.AppendLine("        for (var index = 1; index < keyframes.Length; index++)");
        builder.AppendLine("        {");
        builder.AppendLine("            if (frame < keyframes[index].Frame)");
        builder.AppendLine("            {");
        builder.AppendLine("                break;");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            result = keyframes[index].Value;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return result;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static string EvaluateString(int frame, StringKeyframe[] keyframes, string defaultValue)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (keyframes.Length == 0)");
        builder.AppendLine("        {");
        builder.AppendLine("            return defaultValue;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        var result = keyframes[0].Value;");
        builder.AppendLine("        for (var index = 1; index < keyframes.Length; index++)");
        builder.AppendLine("        {");
        builder.AppendLine("            if (frame < keyframes[index].Frame)");
        builder.AppendLine("            {");
        builder.AppendLine("                break;");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            result = keyframes[index].Value;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return result;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static float Ease(float t, EaseMode mode)");
        builder.AppendLine("    {");
        builder.AppendLine("        return mode switch");
        builder.AppendLine("        {");
        builder.AppendLine("            EaseMode.EaseIn => t * t * t,");
        builder.AppendLine("            EaseMode.EaseOut => 1f - Mathf.Pow(1f - t, 3f),");
        builder.AppendLine("            EaseMode.EaseInOut => t < 0.5f ? 4f * t * t * t : 1f - Mathf.Pow(-2f * t + 2f, 3f) / 2f,");
        builder.AppendLine("            EaseMode.Step => 0f,");
        builder.AppendLine("            _ => t");
        builder.AppendLine("        };");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static void ApplyOpacity(Component target, float value)");
        builder.AppendLine("    {");
        builder.AppendLine("        var canvasGroup = target.GetComponent<CanvasGroup>();");
        builder.AppendLine("        if (canvasGroup == null)");
        builder.AppendLine("        {");
        builder.AppendLine("            canvasGroup = target.gameObject.AddComponent<CanvasGroup>();");
        builder.AppendLine("        }");
        builder.AppendLine("        canvasGroup.alpha = Mathf.Clamp01(value);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static void ApplyWidth(Component target, float value)");
        builder.AppendLine("    {");
        builder.AppendLine("        ResolveRectTransform(target).SetSizeWithCurrentAnchors(RectTransform.Axis.Horizontal, value);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static void ApplyHeight(Component target, float value)");
        builder.AppendLine("    {");
        builder.AppendLine("        ResolveRectTransform(target).SetSizeWithCurrentAnchors(RectTransform.Axis.Vertical, value);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static void ApplyVisibility(Component target, bool visible)");
        builder.AppendLine("    {");
        builder.AppendLine("        target.gameObject.SetActive(visible);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static void ApplyTranslate(Component target, float x, float y)");
        builder.AppendLine("    {");
        builder.AppendLine("        var rectTransform = ResolveRectTransform(target);");
        builder.AppendLine("        var state = BoomHudMotionRectState.Capture(rectTransform);");
        builder.AppendLine("        rectTransform.anchoredPosition = new Vector2(state.AnchoredPosition.x + x, state.AnchoredPosition.y - y);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static void ApplyScale(Component target, float x, float y)");
        builder.AppendLine("    {");
        builder.AppendLine("        var state = BoomHudMotionRectState.Capture(target);");
        builder.AppendLine("        target.transform.localScale = new Vector3(state.LocalScale.x * x, state.LocalScale.y * y, state.LocalScale.z);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static void ApplyRotation(Component target, float degrees)");
        builder.AppendLine("    {");
        builder.AppendLine("        var state = BoomHudMotionRectState.Capture(target);");
        builder.AppendLine("        var euler = state.LocalEulerAngles;");
        builder.AppendLine("        target.transform.localEulerAngles = new Vector3(euler.x, euler.y, euler.z + degrees);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static void ApplyColor(Component target, string value)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (!ColorUtility.TryParseHtmlString(value, out var color))");
        builder.AppendLine("        {");
        builder.AppendLine("            return;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (target is Graphic graphic)");
        builder.AppendLine("        {");
        builder.AppendLine("            graphic.color = color;");
        builder.AppendLine("            return;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (target is Selectable selectable && selectable.targetGraphic != null)");
        builder.AppendLine("        {");
        builder.AppendLine("            selectable.targetGraphic.color = color;");
        builder.AppendLine("            return;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        var attachedGraphic = target.GetComponent<Graphic>();");
        builder.AppendLine("        if (attachedGraphic != null)");
        builder.AppendLine("        {");
        builder.AppendLine("            attachedGraphic.color = color;");
        builder.AppendLine("        }");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static void ApplyText(Component target, string value)");
        builder.AppendLine("    {");
        builder.AppendLine("        switch (target)");
        builder.AppendLine("        {");
        builder.AppendLine("            case Text text:");
        builder.AppendLine("                text.text = value;");
        builder.AppendLine("                return;");
        builder.AppendLine("            case InputField inputField:");
        builder.AppendLine("                inputField.text = value;");
        builder.AppendLine("                if (inputField.textComponent != null)");
        builder.AppendLine("                {");
        builder.AppendLine("                    inputField.textComponent.text = value;");
        builder.AppendLine("                }");
        builder.AppendLine("                return;");
        builder.AppendLine("            case Button button:");
        builder.AppendLine("                SetNestedLabelText(button.gameObject, value);");
        builder.AppendLine("                return;");
        builder.AppendLine("            case Toggle toggle:");
        builder.AppendLine("                SetNestedLabelText(toggle.gameObject, value);");
        builder.AppendLine("                return;");
        builder.AppendLine("        }");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static void SetNestedLabelText(GameObject gameObject, string value)");
        builder.AppendLine("    {");
        builder.AppendLine("        var text = gameObject.GetComponentInChildren<Text>(true);");
        builder.AppendLine("        if (text != null)");
        builder.AppendLine("        {");
        builder.AppendLine("            text.text = value;");
        builder.AppendLine("        }");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static RectTransform ResolveRectTransform(Component target)");
        builder.AppendLine("    {");
        builder.AppendLine("        return target as RectTransform");
        builder.AppendLine("            ?? target.GetComponent<RectTransform>()");
        builder.AppendLine("            ?? throw new InvalidOperationException($\"Motion target '{target.name}' is missing a RectTransform.\");");
        builder.AppendLine("    }");
        builder.AppendLine();
