using System.Globalization;
using System.Linq;
using System.Text;
using BoomHud.Abstractions.Generation;
using BoomHud.Abstractions.IR;
using BoomHud.Abstractions.Motion;

namespace BoomHud.Gen.Unity;

/// <summary>
/// Exports BoomHud Motion JSON into Unity UI Toolkit runtime motion helpers.
/// </summary>
public static class UnityMotionExporter
{
    public static GenerationResult Generate(
        HudDocument document,
        MotionDocument motion,
        GenerationOptions options)
    {
        var planDiagnostics = new List<Diagnostic>();
        var plan = UnityBackendPlanner.CreatePlan(document, options, planDiagnostics);
        var result = Generate(document, plan, motion, options);
        if (planDiagnostics.Count == 0)
        {
            return result;
        }

        return new GenerationResult
        {
            Files = result.Files,
            Diagnostics = planDiagnostics.Concat(result.Diagnostics).ToList()
        };
    }

    internal static GenerationResult Generate(
        HudDocument document,
        UnityBackendPlan plan,
        MotionDocument motion,
        GenerationOptions options)
    {
        var diagnostics = new List<Diagnostic>();
        var files = new List<GeneratedFile>();

        try
        {
            var targetInfos = CollectTargetInfos(plan);
            var code = GenerateMotionClass(document, motion, options, targetInfos, diagnostics);
            files.Add(new GeneratedFile
            {
                Path = $"{document.Name}Motion.gen.cs",
                Content = code,
                Type = GeneratedFileType.SourceCode
            });

            var hostCode = GenerateMotionHostClass(document, plan, options);
            files.Add(new GeneratedFile
            {
                Path = $"{document.Name}MotionHost.gen.cs",
                Content = hostCode,
                Type = GeneratedFileType.SourceCode
            });
        }
        catch (Exception ex)
        {
            diagnostics.Add(Diagnostic.Error($"Unity motion export failed: {ex.Message}", code: "BHU2000"));
        }

        return new GenerationResult
        {
            Files = files,
            Diagnostics = diagnostics
        };
    }

    private static string GenerateMotionClass(
        HudDocument document,
        MotionDocument motion,
        GenerationOptions options,
        Dictionary<string, UnityMotionTargetInfo> targetInfos,
        List<Diagnostic> diagnostics)
    {
        var className = $"{document.Name}Motion";
        var builder = new StringBuilder();

        if (options.IncludeComments)
        {
            builder.AppendLine("// <auto-generated>");
            AppendInvariantLine(builder, $"// Generated by BoomHud.Gen.Unity from motion document {motion.Name}");
            builder.AppendLine("// </auto-generated>");
            builder.AppendLine();
        }

        if (options.UseNullableAnnotations)
        {
            builder.AppendLine("#nullable enable");
            builder.AppendLine();
        }

        builder.AppendLine("using System;");
        builder.AppendLine("using UnityEngine;");
        builder.AppendLine("using UnityEngine.UIElements;");
        builder.AppendLine();
        AppendInvariantLine(builder, $"namespace {options.Namespace}");
        builder.AppendLine("{");
        AppendInvariantLine(builder, $"public static class {className}");
        builder.AppendLine("{");
        AppendInvariantLine(builder, $"    public const int FramesPerSecond = {motion.FramesPerSecond.ToString(CultureInfo.InvariantCulture)};");
        builder.AppendLine();
        AppendInvariantLine(builder, $"    public static bool TryApplyAtFrame({document.Name}View view, string clipId, int frame)");
        builder.AppendLine("    {");
        builder.AppendLine("        ArgumentNullException.ThrowIfNull(view);");
        builder.AppendLine();
        builder.AppendLine("        return clipId switch");
        builder.AppendLine("        {");
        foreach (var clip in motion.Clips)
        {
            AppendInvariantLine(builder, $"            {ToStringLiteral(clip.Id)} => Apply{ToPascalCase(clip.Id)}(view, frame),");
        }

        builder.AppendLine("            _ => false");
        builder.AppendLine("        };");
        builder.AppendLine("    }");
        builder.AppendLine();
        AppendInvariantLine(builder, $"    public static bool TryApplyAtTime({document.Name}View view, string clipId, float timeSeconds)");
        builder.AppendLine("    {");
        builder.AppendLine("        return TryApplyAtFrame(view, clipId, Mathf.Max(0, Mathf.RoundToInt(timeSeconds * FramesPerSecond)));");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    public static int GetClipDurationFrames(string clipId)");
        builder.AppendLine("    {");
        builder.AppendLine("        return clipId switch");
        builder.AppendLine("        {");
        foreach (var clip in motion.Clips)
        {
            AppendInvariantLine(builder, $"            {ToStringLiteral(clip.Id)} => {clip.DurationFrames.ToString(CultureInfo.InvariantCulture)},");
        }

        builder.AppendLine("            _ => 0");
        builder.AppendLine("        };");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    public static float GetClipDurationSeconds(string clipId)");
        builder.AppendLine("    {");
        builder.AppendLine("        return GetClipDurationFrames(clipId) / (float)FramesPerSecond;");
        builder.AppendLine("    }");

        var clipRuntimeInfos = motion.Clips
            .Select(clip => AnalyzeClip(document, clip, targetInfos, diagnostics))
            .ToList();

        foreach (var clipInfo in clipRuntimeInfos)
        {
            foreach (var channelField in clipInfo.ChannelFields)
            {
                builder.AppendLine();
                builder.Append("    private static readonly ");
                builder.Append(channelField.FieldType);
                builder.Append("[] ");
                builder.Append(channelField.FieldName);
                builder.AppendLine(" =");
                builder.AppendLine("    {");
                foreach (var item in channelField.Items)
                {
                    builder.Append("        ");
                    builder.Append(item);
                    builder.AppendLine(",");
                }

                builder.AppendLine("    };");
            }

            builder.AppendLine();
            AppendInvariant(builder, $"    private static bool Apply{ToPascalCase(clipInfo.Clip.Id)}({document.Name}View view, int frame)");
            builder.AppendLine();
            builder.AppendLine("    {");
            AppendInvariant(builder, $"        var localFrame = Mathf.Clamp(frame - {clipInfo.Clip.StartFrame.ToString(CultureInfo.InvariantCulture)}, 0, {clipInfo.Clip.DurationFrames.ToString(CultureInfo.InvariantCulture)});");
            builder.AppendLine();

            foreach (var action in clipInfo.Actions)
            {
                builder.Append("        ");
                builder.Append(action);
                builder.AppendLine();
            }

            builder.AppendLine("        return true;");
            builder.AppendLine("    }");
        }

        AppendHelpers(builder);

        builder.AppendLine("}");
        builder.AppendLine("}");
        return builder.ToString();
    }

    private static string GenerateMotionHostClass(
        HudDocument document,
        UnityBackendPlan plan,
        GenerationOptions options)
    {
        var builder = new StringBuilder();

        if (options.IncludeComments)
        {
            builder.AppendLine("// <auto-generated>");
            AppendInvariantLine(builder, $"// Generated by BoomHud.Gen.Unity from {document.Name} motion host");
            builder.AppendLine("// </auto-generated>");
            builder.AppendLine();
        }

        if (options.UseNullableAnnotations)
        {
            builder.AppendLine("#nullable enable");
            builder.AppendLine();
        }

        builder.AppendLine("using System;");
        builder.AppendLine("using BoomHud.Unity.UIToolkit;");
        builder.AppendLine("using UnityEngine;");
        builder.AppendLine("using UnityEngine.UIElements;");
        builder.AppendLine();
        AppendInvariantLine(builder, $"namespace {options.Namespace}");
        builder.AppendLine("{");
        AppendInvariantLine(builder, $"public partial class {document.Name}MotionHost : BoomHudUiToolkitMotionHost");
        builder.AppendLine("{");
        AppendInvariantLine(builder, $"    private const string VisualTreeResourcePath = {ToStringLiteral($"BoomHudGenerated/{document.Name}View")};");
        AppendInvariantLine(builder, $"    private const string StyleSheetResourcePath = {ToStringLiteral($"BoomHudGenerated/{document.Name}View")};");
        builder.AppendLine();
        AppendInvariantLine(builder, $"    private {document.Name}View? _view;");
        builder.AppendLine();
        AppendInvariantLine(builder, $"    public {document.Name}View View => _view ?? throw new InvalidOperationException(\"The generated view has not been bound yet.\");");
        builder.AppendLine();
        builder.AppendLine("    protected override void BindMotionView(VisualElement root)");
        builder.AppendLine("    {");
        builder.AppendLine("        ArgumentNullException.ThrowIfNull(root);");
        AppendInvariantLine(builder, $"        var generatedRoot = ResolveGeneratedRoot(root);");
        AppendInvariantLine(builder, $"        _view = new {document.Name}View(generatedRoot);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    protected override bool TryApplyMotionAtTime(string clipId, float timeSeconds)");
        builder.AppendLine("    {");
        builder.AppendLine("        return _view != null && " + document.Name + "Motion.TryApplyAtTime(_view, clipId, timeSeconds);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    protected override float GetClipDurationSeconds(string clipId)");
        builder.AppendLine("    {");
        builder.AppendLine("        return " + document.Name + "Motion.GetClipDurationSeconds(clipId);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static VisualElement ResolveGeneratedRoot(VisualElement root)");
        builder.AppendLine("    {");
        builder.AppendLine("        EnsureGeneratedAssets(root);");
        builder.AppendLine();
        AppendInvariantLine(builder, $"        if (string.Equals(root.name, {ToStringLiteral(plan.Root.Name)}, StringComparison.Ordinal))");
        builder.AppendLine("        {");
            builder.AppendLine("            return root;");
        builder.AppendLine("        }");
        builder.AppendLine();
        AppendInvariantLine(builder, $"        return root.Q<{plan.Root.ElementType}>({ToStringLiteral(plan.Root.Name)})");
        AppendInvariantLine(builder, $"            ?? throw new InvalidOperationException(\"Could not find generated root element '{plan.Root.Name}'.\");");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static void EnsureGeneratedAssets(VisualElement root)");
        builder.AppendLine("    {");
        AppendInvariantLine(builder, $"        if (root.Q<{plan.Root.ElementType}>({ToStringLiteral(plan.Root.Name)}) == null");
        builder.AppendLine("            && !string.Equals(root.name, " + ToStringLiteral(plan.Root.Name) + ", StringComparison.Ordinal))");
        builder.AppendLine("        {");
        builder.AppendLine("            var visualTree = Resources.Load<VisualTreeAsset>(VisualTreeResourcePath)");
        builder.AppendLine("                ?? throw new InvalidOperationException($\"Could not load VisualTreeAsset from Resources/{VisualTreeResourcePath}.uxml\");");
        builder.AppendLine();
        builder.AppendLine("            root.Clear();");
        builder.AppendLine("            visualTree.CloneTree(root);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        var styleSheet = Resources.Load<StyleSheet>(StyleSheetResourcePath);");
        builder.AppendLine("        if (styleSheet != null && !root.styleSheets.Contains(styleSheet))");
        builder.AppendLine("        {");
        builder.AppendLine("            root.styleSheets.Add(styleSheet);");
        builder.AppendLine("        }");
        builder.AppendLine("    }");
        builder.AppendLine("}");
        builder.AppendLine("}");
        return builder.ToString();
    }

    private static UnityMotionClipInfo AnalyzeClip(
        HudDocument document,
        MotionClip clip,
        Dictionary<string, UnityMotionTargetInfo> targetInfos,
        List<Diagnostic> diagnostics)
    {
        var fields = new List<UnityMotionFieldInfo>();
        var actions = new List<string>();

        foreach (var track in clip.Tracks)
        {
            if (!TryResolveTarget(track, document, targetInfos, out var targetInfo))
            {
                diagnostics.Add(Diagnostic.Warning(
                    $"Motion target '{track.TargetId}' was not found in HUD document '{document.Name}' and will be skipped.",
                    code: "BHU2001"));
                continue;
            }

            var runtimeChannels = new Dictionary<MotionProperty, UnityMotionChannelInfo>();
            foreach (var channel in track.Channels)
            {
                if (!TryMapProperty(channel.Property, targetInfo.ElementType, diagnostics, track.TargetId))
                {
                    continue;
                }

                if (!TryValidateChannelValueKinds(channel.Property, channel.Keyframes, diagnostics, track.TargetId))
                {
                    continue;
                }

                var fieldName = $"s_{SanitizeIdentifier(clip.Id)}{SanitizeIdentifier(track.Id)}{SanitizeIdentifier(channel.Property.ToString())}";
                var fieldType = channel.Property switch
                {
                    MotionProperty.Visibility => "BooleanKeyframe",
                    MotionProperty.Text or MotionProperty.Color => "StringKeyframe",
                    _ => "NumberKeyframe"
                };
                var items = channel.Keyframes.Select(keyframe => FormatKeyframe(fieldType, keyframe)).ToList();

                fields.Add(new UnityMotionFieldInfo
                {
                    FieldName = fieldName,
                    FieldType = fieldType,
                    Items = items
                });

                runtimeChannels[channel.Property] = new UnityMotionChannelInfo
                {
                    Property = channel.Property,
                    FieldName = fieldName
                };
            }

            AppendActions(actions, targetInfo.Accessor, runtimeChannels);
        }

        return new UnityMotionClipInfo
        {
            Clip = clip,
            ChannelFields = fields,
            Actions = actions
        };
    }

    private static void AppendActions(
        List<string> actions,
        string accessor,
        IReadOnlyDictionary<MotionProperty, UnityMotionChannelInfo> channels)
    {
        if (channels.TryGetValue(MotionProperty.Opacity, out var opacity))
        {
            actions.Add($"ApplyOpacity({accessor}, EvaluateNumber(localFrame, {opacity.FieldName}, 1f));");
        }

        if (channels.TryGetValue(MotionProperty.Width, out var width))
        {
            actions.Add($"ApplyWidth({accessor}, EvaluateNumber(localFrame, {width.FieldName}, 0f));");
        }

        if (channels.TryGetValue(MotionProperty.Height, out var height))
        {
            actions.Add($"ApplyHeight({accessor}, EvaluateNumber(localFrame, {height.FieldName}, 0f));");
        }

        if (channels.TryGetValue(MotionProperty.Visibility, out var visibility))
        {
            actions.Add($"ApplyVisibility({accessor}, EvaluateBoolean(localFrame, {visibility.FieldName}, true));");
        }

        if (channels.TryGetValue(MotionProperty.Text, out var text))
        {
            actions.Add($"ApplyText({accessor}, EvaluateString(localFrame, {text.FieldName}, string.Empty));");
        }

        if (channels.TryGetValue(MotionProperty.Color, out var color))
        {
            actions.Add($"ApplyColor({accessor}, EvaluateString(localFrame, {color.FieldName}, string.Empty));");
        }

        var hasPositionX = channels.TryGetValue(MotionProperty.PositionX, out var positionX);
        var hasPositionY = channels.TryGetValue(MotionProperty.PositionY, out var positionY);
        if (hasPositionX || hasPositionY)
        {
            var xExpression = hasPositionX
                ? $"EvaluateNumber(localFrame, {positionX!.FieldName}, 0f)"
                : "0f";
            var yExpression = hasPositionY
                ? $"EvaluateNumber(localFrame, {positionY!.FieldName}, 0f)"
                : "0f";
            actions.Add($"ApplyTranslate({accessor}, {xExpression}, {yExpression});");
        }

        var hasScaleX = channels.TryGetValue(MotionProperty.ScaleX, out var scaleX);
        var hasScaleY = channels.TryGetValue(MotionProperty.ScaleY, out var scaleY);
        if (hasScaleX || hasScaleY)
        {
            var xExpression = hasScaleX
                ? $"EvaluateNumber(localFrame, {scaleX!.FieldName}, 1f)"
                : "1f";
            var yExpression = hasScaleY
                ? $"EvaluateNumber(localFrame, {scaleY!.FieldName}, 1f)"
                : "1f";
            actions.Add($"ApplyScale({accessor}, {xExpression}, {yExpression});");
        }

        if (channels.TryGetValue(MotionProperty.Rotation, out var rotation))
        {
            actions.Add($"ApplyRotation({accessor}, EvaluateNumber(localFrame, {rotation.FieldName}, 0f));");
        }
    }

    private static bool TryResolveTarget(
        MotionTrack track,
        HudDocument document,
        IReadOnlyDictionary<string, UnityMotionTargetInfo> targetInfos,
        out UnityMotionTargetInfo targetInfo)
    {
        if (track.TargetKind == MotionTargetKind.Root)
        {
            return targetInfos.TryGetValue(document.Root.Id ?? "__root__", out targetInfo!);
        }

        return targetInfos.TryGetValue(track.TargetId, out targetInfo!);
    }

    private static Dictionary<string, UnityMotionTargetInfo> CollectTargetInfos(UnityBackendPlan plan)
    {
        var targets = new Dictionary<string, UnityMotionTargetInfo>(StringComparer.Ordinal);

        void Traverse(UnityPlannedNode node, string accessor)
        {
            if (!string.IsNullOrWhiteSpace(node.Source.Id))
            {
                targets[node.Source.Id!] = new UnityMotionTargetInfo
                {
                    Accessor = accessor,
                    ElementType = node.ElementType
                };
            }

            foreach (var child in node.Children)
            {
                Traverse(child, $"view.{child.Name}");
            }
        }

        Traverse(plan.Root, "view.Root");

        if (string.IsNullOrWhiteSpace(plan.Root.Source.Id))
        {
            targets["__root__"] = new UnityMotionTargetInfo
            {
                Accessor = "view.Root",
                ElementType = plan.Root.ElementType
            };
        }

        return targets;
    }

    private static bool TryMapProperty(
        MotionProperty property,
        string elementType,
        List<Diagnostic> diagnostics,
        string targetId)
    {
        switch (property)
        {
            case MotionProperty.Opacity:
            case MotionProperty.PositionX:
            case MotionProperty.PositionY:
            case MotionProperty.ScaleX:
            case MotionProperty.ScaleY:
            case MotionProperty.Rotation:
            case MotionProperty.Width:
            case MotionProperty.Height:
            case MotionProperty.Visibility:
            case MotionProperty.Color:
                return true;
            case MotionProperty.Text:
                if (SupportsTextMutation(elementType))
                {
                    return true;
                }

                diagnostics.Add(Diagnostic.Warning(
                    $"Unity motion export does not support text mutation on target '{targetId}' ({elementType}).",
                    code: "BHU2002"));
                return false;
            default:
                diagnostics.Add(Diagnostic.Warning(
                    $"Unity motion export does not support property '{property}' on target '{targetId}' yet.",
                    code: "BHU2002"));
                return false;
        }
    }

    private static bool SupportsTextMutation(string elementType)
        => string.Equals(elementType, "Label", StringComparison.Ordinal)
            || string.Equals(elementType, "Button", StringComparison.Ordinal)
            || string.Equals(elementType, "TextField", StringComparison.Ordinal)
            || string.Equals(elementType, "ProgressBar", StringComparison.Ordinal);

    private static bool TryValidateChannelValueKinds(
        MotionProperty property,
        IReadOnlyList<MotionKeyframe> keyframes,
        List<Diagnostic> diagnostics,
        string targetId)
    {
        foreach (var keyframe in keyframes)
        {
            var valid = property switch
            {
                MotionProperty.Visibility => keyframe.Value.Kind == MotionValueKind.Boolean,
                MotionProperty.Text or MotionProperty.Color => keyframe.Value.Kind == MotionValueKind.Text,
                _ => keyframe.Value.Kind == MotionValueKind.Number
            };

            if (!valid)
            {
                diagnostics.Add(Diagnostic.Warning(
                    $"Unity motion export skipped property '{property}' on target '{targetId}' because keyframe values are not in an exportable format.",
                    code: "BHU2003"));
                return false;
            }
        }

        return true;
    }

    private static string FormatKeyframe(string fieldType, MotionKeyframe keyframe)
        => fieldType switch
        {
            "BooleanKeyframe" => $"new BooleanKeyframe({keyframe.Frame.ToString(CultureInfo.InvariantCulture)}, {FormatBoolean(keyframe.Value.Boolean ?? false)})",
            "StringKeyframe" => $"new StringKeyframe({keyframe.Frame.ToString(CultureInfo.InvariantCulture)}, {ToStringLiteral(keyframe.Value.Text ?? string.Empty)})",
            _ => $"new NumberKeyframe({keyframe.Frame.ToString(CultureInfo.InvariantCulture)}, {FormatFloat((float)(keyframe.Value.Number ?? 0d))}, EaseMode.{keyframe.Easing})"
        };

    private static void AppendHelpers(StringBuilder builder)
    {
        builder.AppendLine();
        builder.AppendLine("    private static float EvaluateNumber(int frame, NumberKeyframe[] keyframes, float defaultValue)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (keyframes.Length == 0)");
        builder.AppendLine("        {");
        builder.AppendLine("            return defaultValue;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (frame <= keyframes[0].Frame)");
        builder.AppendLine("        {");
        builder.AppendLine("            return keyframes[0].Value;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        for (var index = 0; index < keyframes.Length - 1; index++)");
        builder.AppendLine("        {");
        builder.AppendLine("            var current = keyframes[index];");
        builder.AppendLine("            var next = keyframes[index + 1];");
        builder.AppendLine("            if (frame > next.Frame)");
        builder.AppendLine("            {");
        builder.AppendLine("                continue;");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            if (current.Easing == EaseMode.Step || Mathf.Approximately(current.Value, next.Value))");
        builder.AppendLine("            {");
        builder.AppendLine("                return current.Value;");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            var t = Mathf.InverseLerp(current.Frame, next.Frame, frame);");
        builder.AppendLine("            return Mathf.LerpUnclamped(current.Value, next.Value, Ease(t, current.Easing));");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return keyframes[keyframes.Length - 1].Value;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static bool EvaluateBoolean(int frame, BooleanKeyframe[] keyframes, bool defaultValue)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (keyframes.Length == 0)");
        builder.AppendLine("        {");
        builder.AppendLine("            return defaultValue;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        var result = keyframes[0].Value;");
        builder.AppendLine("        for (var index = 1; index < keyframes.Length; index++)");
        builder.AppendLine("        {");
        builder.AppendLine("            if (frame < keyframes[index].Frame)");
        builder.AppendLine("            {");
        builder.AppendLine("                break;");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            result = keyframes[index].Value;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return result;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static string EvaluateString(int frame, StringKeyframe[] keyframes, string defaultValue)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (keyframes.Length == 0)");
        builder.AppendLine("        {");
        builder.AppendLine("            return defaultValue;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        var result = keyframes[0].Value;");
        builder.AppendLine("        for (var index = 1; index < keyframes.Length; index++)");
        builder.AppendLine("        {");
        builder.AppendLine("            if (frame < keyframes[index].Frame)");
        builder.AppendLine("            {");
        builder.AppendLine("                break;");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            result = keyframes[index].Value;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return result;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static float Ease(float t, EaseMode mode)");
        builder.AppendLine("    {");
        builder.AppendLine("        return mode switch");
        builder.AppendLine("        {");
        builder.AppendLine("            EaseMode.EaseIn => t * t * t,");
        builder.AppendLine("            EaseMode.EaseOut => 1f - Mathf.Pow(1f - t, 3f),");
        builder.AppendLine("            EaseMode.EaseInOut => t < 0.5f ? 4f * t * t * t : 1f - Mathf.Pow(-2f * t + 2f, 3f) / 2f,");
        builder.AppendLine("            EaseMode.Step => 0f,");
        builder.AppendLine("            _ => t");
        builder.AppendLine("        };");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static void ApplyOpacity(VisualElement element, float value)");
        builder.AppendLine("    {");
        builder.AppendLine("        element.style.opacity = value;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static void ApplyWidth(VisualElement element, float value)");
        builder.AppendLine("    {");
        builder.AppendLine("        element.style.width = value;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static void ApplyHeight(VisualElement element, float value)");
        builder.AppendLine("    {");
        builder.AppendLine("        element.style.height = value;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static void ApplyVisibility(VisualElement element, bool visible)");
        builder.AppendLine("    {");
        builder.AppendLine("        element.style.display = visible ? DisplayStyle.Flex : DisplayStyle.None;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static void ApplyTranslate(VisualElement element, float x, float y)");
        builder.AppendLine("    {");
        builder.AppendLine("        element.style.translate = new StyleTranslate");
        builder.AppendLine("        {");
        builder.AppendLine("            value = new Translate");
        builder.AppendLine("            {");
        builder.AppendLine("                x = Length.Pixels(x),");
        builder.AppendLine("                y = Length.Pixels(y),");
        builder.AppendLine("                z = 0f");
        builder.AppendLine("            }");
        builder.AppendLine("        };");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static void ApplyScale(VisualElement element, float x, float y)");
        builder.AppendLine("    {");
        builder.AppendLine("        element.style.scale = new StyleScale");
        builder.AppendLine("        {");
        builder.AppendLine("            value = new Scale");
        builder.AppendLine("            {");
        builder.AppendLine("                value = new Vector3(x, y, 1f)");
        builder.AppendLine("            }");
        builder.AppendLine("        };");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static void ApplyRotation(VisualElement element, float degrees)");
        builder.AppendLine("    {");
        builder.AppendLine("        element.style.rotate = new StyleRotate");
        builder.AppendLine("        {");
