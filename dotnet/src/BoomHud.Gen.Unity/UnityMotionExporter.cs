        => builder.Append(FormattableString.Invariant(value));

    private static string FormatFloat(float value)
        => value.ToString("0.###", CultureInfo.InvariantCulture) + "f";

    private static string ToStringLiteral(string value)
        => "\"" + value
            .Replace("\\", "\\\\", StringComparison.Ordinal)
            .Replace("\"", "\\\"", StringComparison.Ordinal)
            .Replace("\r", "\\r", StringComparison.Ordinal)
            .Replace("\n", "\\n", StringComparison.Ordinal) + "\"";

    private static string ToPascalCase(string value)
    {
        var parts = value.Split(['.', ':', '-', '/', ' '], StringSplitOptions.RemoveEmptyEntries | StringSplitOptions.TrimEntries)
            .Select(static part => new string(part.Where(char.IsLetterOrDigit).ToArray()))
            .Where(static part => part.Length > 0)
            .ToList();

        if (parts.Count == 0)
        {
            return "Motion";
        }

        return string.Concat(parts.Select(static part => char.ToUpperInvariant(part[0]) + part[1..]));
    }

    private static string SanitizeIdentifier(string value)
    {
        var filtered = new string(value.Where(char.IsLetterOrDigit).ToArray());
        if (string.IsNullOrEmpty(filtered))
        {
            return "Value";
        }

        return char.ToUpperInvariant(filtered[0]) + filtered[1..];
    }

