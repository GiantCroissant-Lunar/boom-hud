using System.Globalization;
using BoomHud.Abstractions.Generation;
using BoomHud.Abstractions.IR;
using BoomHud.Abstractions.Motion;
using BoomHud.Generators;

namespace BoomHud.Gen.Godot;

/// <summary>
/// Exports BoomHud Motion JSON into Godot AnimationPlayer construction code.
/// </summary>
public static class GodotMotionExporter
{
    public static GenerationResult Generate(HudDocument document, MotionDocument motion, GenerationOptions options)
    {
        var diagnostics = new List<Diagnostic>();
        var files = new List<GeneratedFile>();

        AddPortableMotionDiagnostics(motion, diagnostics);

        try
        {
            var scenePaths = CollectScenePaths(document.Root);
            var code = GenerateMotionClass(document, motion, options, scenePaths, diagnostics);
            files.Add(new GeneratedFile
            {
                Path = $"{document.Name}Motion.g.cs",
                Content = code,
                Type = GeneratedFileType.SourceCode
            });
        }
        catch (Exception ex)
        {
            diagnostics.Add(Diagnostic.Error($"Godot motion export failed: {ex.Message}", code: "BHG2000"));
        }

        return new GenerationResult
        {
            Files = files,
            Diagnostics = diagnostics
        };
    }

    private static string GenerateMotionClass(
        HudDocument document,
        MotionDocument motion,
        GenerationOptions options,
        Dictionary<string, string> scenePaths,
        List<Diagnostic> diagnostics)
    {
        var className = $"{document.Name}Motion";
        var cb = new CodeBuilder();

        if (options.IncludeComments)
        {
            cb.AppendLine("// <auto-generated>");
            cb.AppendLine($"// Generated by BoomHud.Gen.Godot from motion document {motion.Name}");
            cb.AppendLine("// </auto-generated>");
            cb.AppendLine();
        }

        if (options.UseNullableAnnotations)
        {
            cb.AppendLine("#nullable enable");
            cb.AppendLine();
        }

        cb.AppendLine("using Godot;");
        cb.AppendLine("using System;");
        cb.AppendLine();
        cb.AppendLine($"namespace {options.Namespace};");
        cb.AppendLine();
        cb.AppendLine($"public static class {className}");
        cb.OpenBlock();
        cb.AppendLine("public const string DefaultAnimationPlayerName = \"BoomHudMotionPlayer\";");
        cb.AppendLine();
        cb.AppendLine("public static AnimationPlayer Attach(Node owner, string animationPlayerName = DefaultAnimationPlayerName)");
        cb.OpenBlock();
        cb.AppendLine("ArgumentNullException.ThrowIfNull(owner);");
        cb.AppendLine("var existing = owner.GetNodeOrNull<AnimationPlayer>(animationPlayerName);");
        cb.AppendLine("if (existing != null)");
        cb.OpenBlock();
        cb.AppendLine("return existing;");
        cb.CloseBlock();
        cb.AppendLine("var player = new AnimationPlayer();");
        cb.AppendLine("player.Name = animationPlayerName;");
        foreach (var clip in motion.Clips)
        {
            cb.AppendLine($"player.AddAnimation({Literal(clip.Id)}, Build{ToPascalCase(clip.Id)}());");
        }
        cb.AppendLine("owner.AddChild(player);");
        cb.AppendLine("return player;");
        cb.CloseBlock();

        foreach (var clip in motion.Clips)
        {
            cb.AppendLine();
            cb.AppendLine($"private static Animation Build{ToPascalCase(clip.Id)}()");
            cb.OpenBlock();
            cb.AppendLine("var animation = new Animation();");
            cb.AppendLine($"animation.Length = {FormatNumber(clip.DurationFrames / (double)motion.FramesPerSecond)};");
            cb.AppendLine($"animation.Step = {FormatNumber(1d / motion.FramesPerSecond)};");
            foreach (var track in clip.Tracks)
            {
                if (!scenePaths.TryGetValue(track.TargetId, out var scenePath))
                {
                    diagnostics.Add(Diagnostic.Warning(
                        $"Motion target '{track.TargetId}' was not found in HUD document '{document.Name}' and will be skipped.",
                        code: "BHG2001"));
                    continue;
                }

                foreach (var channel in track.Channels)
                {
                    var propertyPath = TryMapProperty(channel.Property, diagnostics, track.TargetId);
                    if (propertyPath == null)
                    {
                        continue;
                    }

                    if (!HasPortableChannelValueKinds(channel.Property, channel.Keyframes))
                    {
                        continue;
                    }

                    var trackVar = $"track{SanitizeIdentifier(track.Id)}{SanitizeIdentifier(channel.Property.ToString())}";
                    cb.AppendLine($"var {trackVar} = animation.AddTrack(Animation.TrackType.Value);");
                    cb.AppendLine($"animation.TrackSetPath({trackVar}, new NodePath({Literal(ToGodotTrackPath(scenePath, propertyPath))}));");
                    foreach (var keyframe in channel.Keyframes)
                    {
                        cb.AppendLine(
                            $"animation.TrackInsertKey({trackVar}, {FormatNumber(keyframe.Frame / (double)motion.FramesPerSecond)}, {FormatValue(channel.Property, keyframe.Value)});");
                    }
                }
            }
            cb.AppendLine("return animation;");
            cb.CloseBlock();
        }

        cb.CloseBlock();
        return cb.ToString();
    }

    private static Dictionary<string, string> CollectScenePaths(ComponentNode root)
    {
        var paths = new Dictionary<string, string>(StringComparer.Ordinal);

        void Traverse(ComponentNode node, string currentPath)
        {
            if (!string.IsNullOrWhiteSpace(node.Id))
            {
                paths.TryAdd(node.Id!, currentPath);
            }

            foreach (var child in node.Children)
            {
                if (child.Type == ComponentType.MenuItem)
                {
                    continue;
                }

                var segment = child.Id;
                if (string.IsNullOrWhiteSpace(segment))
                {
                    continue;
                }

                var nextPath = string.IsNullOrEmpty(currentPath) ? segment! : $"{currentPath}/{segment}";
                Traverse(child, nextPath);
            }
        }

        Traverse(root, string.Empty);
        return paths;
    }

    private static string? TryMapProperty(MotionProperty property, List<Diagnostic> diagnostics, string targetId)
        => property switch
        {
            MotionProperty.Opacity => "modulate:a",
            MotionProperty.PositionX => "position:x",
            MotionProperty.PositionY => "position:y",
            MotionProperty.ScaleX => "scale:x",
            MotionProperty.ScaleY => "scale:y",
            MotionProperty.Rotation => "rotation_degrees",
            MotionProperty.Width => "size:x",
            MotionProperty.Height => "size:y",
            MotionProperty.Visibility => "visible",
            MotionProperty.Text => "text",
            MotionProperty.Color => "modulate",
            MotionProperty.PositionZ or MotionProperty.ScaleZ or MotionProperty.RotationX or MotionProperty.RotationY or MotionProperty.SpriteFrame
                => WarnUnsupported(property, targetId, diagnostics),
            _ => WarnUnsupported(property, targetId, diagnostics)
        };

    private static string? WarnUnsupported(MotionProperty property, string targetId, List<Diagnostic> diagnostics)
    {
        diagnostics.Add(Diagnostic.Warning(
            $"Godot motion export does not support property '{property}' on target '{targetId}' yet.",
            code: "BHG2002"));
        return null;
    }

    private static bool HasPortableChannelValueKinds(MotionProperty property, IReadOnlyList<MotionKeyframe> keyframes)
        => keyframes.All(keyframe => MotionDocument.IsPortableValueKind(property, keyframe.Value.Kind));

    private static void AddPortableMotionDiagnostics(MotionDocument motion, List<Diagnostic> diagnostics)
    {
        var existing = new HashSet<(string Code, string Message)>(
            motion.LoadDiagnostics.Select(diagnostic => (diagnostic.Code, diagnostic.Message)));

        foreach (var diagnostic in motion.ValidatePortableContract())
        {
            if (!existing.Add((diagnostic.Code, diagnostic.Message)))
            {
                continue;
            }

            diagnostics.Add(Diagnostic.Warning(diagnostic.Message, diagnostic.SourceFile, diagnostic.Code));
        }
    }

    private static string ToGodotTrackPath(string scenePath, string propertyPath)
    {
        var basePath = string.IsNullOrEmpty(scenePath) ? ".." : $"../{scenePath}";
        return $"{basePath}:{propertyPath}";
    }

    private static string FormatValue(MotionProperty property, MotionValue value)
        => property switch
        {
            MotionProperty.Visibility => value.Boolean is true ? "true" : "false",
            MotionProperty.Text => Literal(value.Text ?? string.Empty),
            MotionProperty.Color => $"Color.FromString({Literal(value.Text ?? "#ffffff")}, Colors.White)",
            _ => FormatNumber(value.Number ?? 0)
        };

    private static string FormatNumber(double value)
        => value.ToString("0.######", CultureInfo.InvariantCulture);

    private static string Literal(string value)
        => "\"" + value
            .Replace("\\", "\\\\", StringComparison.Ordinal)
            .Replace("\"", "\\\"", StringComparison.Ordinal)
            .Replace("\r", "\\r", StringComparison.Ordinal)
            .Replace("\n", "\\n", StringComparison.Ordinal) + "\"";

    private static string ToPascalCase(string value)
    {
        var parts = value.Split(['.', ':', '-', '/', ' '], StringSplitOptions.RemoveEmptyEntries | StringSplitOptions.TrimEntries)
            .Select(static part => new string(part.Where(char.IsLetterOrDigit).ToArray()))
            .Where(static part => part.Length > 0)
            .ToList();

        if (parts.Count == 0)
        {
            return "Motion";
        }

        return string.Concat(parts.Select(static part => char.ToUpperInvariant(part[0]) + part[1..]));
    }

    private static string SanitizeIdentifier(string value)
    {
        var filtered = new string(value.Where(char.IsLetterOrDigit).ToArray());
        if (string.IsNullOrEmpty(filtered))
        {
            return "Value";
        }

        return char.ToUpperInvariant(filtered[0]) + filtered[1..];
    }
}
