    private static bool IsPartial(INamedTypeSymbol symbol)
    {
        foreach (var decl in symbol.DeclaringSyntaxReferences)
        {
            if (decl.GetSyntax() is TypeDeclarationSyntax tds &&
                tds.Modifiers.Any(m => m.IsKind(SyntaxKind.PartialKeyword)))
            {
                return true;
            }
        }

        return false;
    }

    private static AttributeData? GetAttribute(INamedTypeSymbol symbol, string metadataName)
    {
        foreach (var attr in symbol.GetAttributes())
        {
            if (attr.AttributeClass is null)
                continue;

            if (string.Equals(attr.AttributeClass.ToDisplayString(), metadataName, StringComparison.Ordinal) ||
                string.Equals(attr.AttributeClass.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat), "global::" + metadataName, StringComparison.Ordinal))
            {
                return attr;
            }
        }

        return null;
    }

    private static string? GetNamespace(INamedTypeSymbol symbol)
    {
        var ns = symbol.ContainingNamespace;
        if (ns is null || ns.IsGlobalNamespace)
            return null;

        return ns.ToDisplayString();
    }

    private static string GetAccessibility(INamedTypeSymbol symbol)
    {
        return symbol.DeclaredAccessibility switch
        {
            Accessibility.Public => "public",
            Accessibility.Internal => "internal",
            _ => "internal"
        };
    }

    private const string AttributeSource = @"namespace BoomHud.Mvvm
{
    [global::System.AttributeUsage(global::System.AttributeTargets.Class, AllowMultiple = true, Inherited = false)]
    internal sealed class BoomHudViewModelForAttribute : global::System.Attribute
    {
        public BoomHudViewModelForAttribute(string viewName)
        {
            ViewName = viewName;
        }

        public string ViewName { get; }
    }
}
";
}
